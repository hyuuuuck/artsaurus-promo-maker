export function normalizePosterFont(value: string | undefined) {
  const trimmed = value?.trim();
  if (!trimmed) return "poster-nanum-gothic";
  return posterFontOptions.find((font) => font.value === trimmed || font.label === trimmed)?.value ?? legacyFontMap[trimmed] ?? "poster-nanum-gothic";
}

export function posterBrowserFontStack(value: string | undefined) {
  const normalized = normalizePosterFont(value);
  return posterFontOptions.find((font) => font.value === normalized)?.browserStack ?? posterFontOptions[0].browserStack;
}

export function posterExportFontStack(value: string | undefined) {
  const normalized = normalizePosterFont(value);
  return posterFontOptions.find((font) => font.value === normalized)?.exportStack ?? posterFontOptions[0].exportStack;
}
